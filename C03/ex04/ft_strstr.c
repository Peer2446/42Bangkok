char	*ft_strstr(char *str, char *to_find)
{
	int	j;

	while (*str != '\0')
	{
		j = 0;
		while (str[j] == to_find[j] && to_find[j] != '\0' && str[j] != '\0')
		{
			j++;
		}
		if (to_find[j] == '\0')
		{
			return (str);
		}
		str++;
	}
	return (0);
}
